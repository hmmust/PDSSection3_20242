import numpy as np
print(np.random.rand())
print(np.random.rand(5))
print(np.random.randint(10)) # 0-10
print(np.random.randint(10,20)) # 10-20
print(np.random.randint(low=10,high=20)) # 10-20
print(np.random.randint(low=10,high=20,size=5)) # 10-20
print(np.random.randint(low=10,high=20,size=(5,5))) # 10-20

